def _match(crop: str):
    """Resolve a free-text crop string to an EPV entry by stem; None if no thresholds."""
    c = (crop or "").strip().lower()
    if not c:
        return None
    if "пшениц" in c:
        if "озим" in c:
            return EPV["пшеница озимая"]
        if "яров" in c:
            return EPV["пшеница яровая"]
        return EPV["пшеница озимая"]  # неуточнённая пшеница → озимая (в ЦЧР доминирует)
    if "подсолн" in c:
        return EPV["подсолнечник"]
    if c.startswith("со") and ("соя" in c or "сои" in c or "сое" in c):
        return EPV["соя"]
    if "соя" in c:
        return EPV["соя"]
    return None


def epv_block(crop: str) -> str:
    """Prompt block with the farm's own ЭПВ for this crop (or '' if none on file)."""
    e = _match(crop)
    if not e:
        return ""
    lines = [
        f"ЭПВ ХОЗЯЙСТВА (пороги вредоносности от гл. агронома Алмаса, культура «{e['label']}»). "
        "Это ГЛАВНЫЙ ориентир для решения «обрабатывать сейчас или ждать» и «хватит одной обработки или нужна "
        "вторая». Опирайся на ЭТИ пороги хозяйства, а не на общие справочные значения.",
        "Сорняки (группа → ЭПВ → фаза учёта → ориентир по числу обработок):",
    ]
    for g, epv, phase, passes in e["weeds"]:
        lines.append(f"• {g}: ЭПВ {epv}; учёт в фазу {phase}; обработок: {passes}.")
    if e.get("pests"):
        lines.append(f"Вредители: {e['pests']}.")
    if e.get("diseases"):
        lines.append(f"Болезни: {e['diseases']}.")
    return "\n".join(lines)
